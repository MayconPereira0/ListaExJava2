import java.util.Scanner;

public class Exercicio01 {
    public static void main(String[] args) {
        System.out.println("=== EXERCÍCIO 1 - CONTAGEM REGRESSIVA ===");
        
        int contador = 10;
        
        while (contador >= 0) {
            System.out.println(contador);
            contador--;
        }
        
        System.out.println("FIM!");
    }
}
